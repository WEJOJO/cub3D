#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*tmp;
	size_t			pos;

	tmp = (unsigned char *)s;
	pos = 0;
	while (pos < n)
		tmp[pos++] = c;
	return (s);
}
